interface GenerateTokenInput {
  id: string;
  name: string;
  email: string;
  role: Role;
}

export const generateToken = (data: GenerateTokenInput): string => {
  const payload: TokenPayload = {
    id: data.id,
    name: data.name,
    email: data.email,
    role: data.role,
  };

  const expiresIn = TOKEN_EXPIRY[data.role];

  return jwt.sign(payload, JWT_SECRET, { expiresIn });
};

