export const getTokenExpiration = (token: string): Date | null => {
  try {
    const decoded = jwt.decode(token) as TokenPayload & { exp?: number };

    if (!decoded?.exp) return null;

    return new Date(decoded.exp * 1000);
  } catch {
    return null;
  }
};