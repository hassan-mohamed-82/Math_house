export const generateToken = (data: {
  id: string;
  name: string;
  role: Role;
}): string => {
  const payload: TokenPayload = {
    id: data.id,
    name: data.name,
    role: data.role,
  };

  // admin = 7 days, user = 30 days
  const expiresIn = data.role === 'admin' ? '7d' : '30d';

  return jwt.sign(payload, JWT_SECRET, { expiresIn });
};

